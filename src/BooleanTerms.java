public class BooleanTerms {
    // Ternary operator
    public static void main(){
        boolean beautifulGirlNextToMyHouse = true;
        String iHaveAChance = beautifulGirlNextToMyHouse ? "I have a chance" : "No chance at all";
        System.out.println(iHaveAChance);
        System.out.println(other());
    }

    public static String other(){
        return "Work please";
    }
}
